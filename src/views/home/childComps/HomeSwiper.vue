<template>
    <swiper>
        <swiper-item v-for="item in banners" >
            <a :href="item.link">
                <img :src="item.image" alt=""  @load="imageLoad">
            </a>
        </swiper-item>
    </swiper>
</template>

<script>
    import {Swiper,SwiperItem} from 'components/common/swiper'
export default {
    name:"HomeSwiper",
    props:{
        banners:{
            type:Array,
            default(){
                return []
            }
        }
    },
    data() {
      return {
        //为了不让HomeLoad多次发出时间，可以使用isLoad的变量来进行状态的记录
        isLoad: false
      }
    },
    components:{
        Swiper,
        SwiperItem
    },
    methods: {
      imageLoad() {
        if (!this.isLoad) {
          this.$emit('swiperImageLoad')
          this.isLoad = true
        }
      }
    }
}
</script>

<style scoped>
    img{
        width:100%
    }
</style>